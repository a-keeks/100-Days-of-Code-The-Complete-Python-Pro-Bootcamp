# Your Life in Weeks
# calulates how many weeks left till we reach the age of 90

age = (input("How old are you? \n "))

Years_left = 90 - age #Calculates number of years till the turn 90

Weeks_left = Years_left * 52 #Converts to weeks

print(f"You have {Weeks_left}  weeks till you turn 90.")
